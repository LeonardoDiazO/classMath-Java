package ClaseMath;

public class Ejercicio_2 {

	public static void main(String[] args) {
//		Metodo utilizado para hallar la raíz cuadrada de los siguientes
//		números por medio del método Math.sqrt.
		
		double n1 = Math.sqrt(2);
		double n2 = Math.sqrt(8);
		double n3 = Math.sqrt(9);
		double n4 = Math.sqrt(27);
		double n5 = Math.sqrt(28);
		double n6 = Math.sqrt(55);
		double n7 = Math.sqrt(121);
		
		System.out.println("La raiz cuadrada del numero 2 es: " + n1);
		System.out.println("La raiz cuadrada del numero 8 es: " + n2);
		System.out.println("La raiz cuadrada del numero 9 es: " + n3);
		System.out.println("La raiz cuadrada del numero 27 es: " + n4);
		System.out.println("La raiz cuadrada del numero 28 es: " + n5);
		System.out.println("La raiz cuadrada del numero 55 es: " + n6);
		System.out.println("La raiz cuadrada del numero 121 es: " + n7);
	}

}
